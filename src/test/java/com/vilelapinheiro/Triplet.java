package com.vilelapinheiro;

public class Triplet<T> {
    private T value1, value2, value3;

    public Triplet(T value1, T value2, T value3) {
        this.value1 = value1;
        this.value2 = value2;
        this.value3 = value3;
    }

    public T getValue1() {
        return value1;
    }

    public T getValue2() {
        return value2;
    }

    public T getValue3() {
        return value3;
    }
}